var a = 'a'
var h = 'h'
/* 'a'보다 뒤에 있는 다섯 번째 글자 */ println(a + 5)  // f
/* 'a'보다 앞에 있는 다섯 번째 글자 */ println(a - 5)  // \
/* 'a'와 'h' 사이의 거리 */ println(h - a)            // 7
/* 'h' 바로 앞 글자      */ println(--h)              // g
/* 'a' 바로 뒤 글자      */ println(++a)              // b